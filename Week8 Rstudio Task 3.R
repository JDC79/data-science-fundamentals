#Task 3. Write a R program to create a sequence of numbers from 20 to 50 and find the mean of numbers from 20 to 60 and sum of numbers from 51 to 91.
print("Here is a sequence of numbers from 20 to 50:")
print(seq(20,50))
print("The Mean of the numbers from 20 to 60 is:")
print(mean(20:60))
print("The Sum of numbers from 51 to 91 is:")
print(sum(51:91))
